-- map Clerk JWT sub to internal user ID
create or replace function public.requesting_user_id()
returns bigint
language sql
stable
security definer
set search_path = 'public'
as $$
  select user_id
  from public.users
  where user_clerk_id = (auth.jwt() ->> 'sub')
  limit 1;
$$;

-- is the current user the owner of this category?
create or replace function public.is_category_owner(category_id uuid)
returns boolean
language sql
stable
security definer
set search_path = 'public'
as $$
  select exists (
    select 1 from public.categories c
    where c.category_id = $1
      and c.category_owner_id = requesting_user_id()
  );
$$;

-- is the current user a collaborator of this category?
create or replace function public.is_category_collaborator(category_id uuid)
returns boolean
language sql
stable
security definer
set search_path = 'public'
as $$
  select exists (
    select 1
    from public.invitations i
    join public.collaborators col on i.invitation_id = col.invitation_id
    where i.invitation_category_id = $1
      and col.user_id = requesting_user_id()
  );
$$;

-- full task access check (default / unshared / shared)
create or replace function public.can_access_task(task_id uuid)
returns boolean
language plpgsql
stable
security definer
set search_path = 'public'
as $$
declare
  v_category_id uuid;
  v_owner_id bigint;
begin
  select t.task_category_id, t.task_owner_id
    into v_category_id, v_owner_id
    from public.tasks t
    where t.task_id = $1;

  -- default category: only the task owner
  if v_category_id = '00000000-0000-0000-0000-000000000000' then
    return v_owner_id = requesting_user_id();
  end if;

  -- custom categories: owner or collaborator can access
  return is_category_owner(v_category_id) or is_category_collaborator(v_category_id);
end;
$$;

