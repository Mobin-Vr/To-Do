create table public.users (
  user_id bigint generated by default as identity primary key,
  user_email text not null unique,
  user_fullname text not null,
  user_clerk_id text unique,
  user_created_at timestamp with time zone default now()
);

create table public.categories (
  category_owner_id bigint references public.users(user_id),
  category_title text not null,
  category_created_at timestamp with time zone default now(),
  has_category_invitation boolean not null default false,
  category_id uuid not null primary key,
  has_category_collaborator boolean not null default false
);

create table public.tasks (
  task_title text not null,
  task_note text,
  is_task_starred boolean default false,
  task_due_date timestamp with time zone,
  task_created_at timestamp with time zone,
  task_updated_at timestamp with time zone,
  is_task_completed boolean default false,
  task_id uuid not null primary key,
  task_reminder timestamp with time zone,
  task_repeat text,
  task_completed_at timestamp with time zone,
  is_task_in_myday boolean default false,
  task_steps jsonb,
  task_owner_id bigint not null references public.users(user_id),
  task_category_id uuid not null references public.categories(category_id) on delete cascade,
  task_category_title text
);

create table public.invitations (
  invitation_id uuid not null default gen_random_uuid() primary key,
  invitation_category_id uuid not null references public.categories(category_id) on delete cascade,
  invitation_owner_id bigint not null references public.users(user_id),
  invitation_created_at timestamp with time zone default now(),
  invitation_limit_access boolean not null default false
);

create table public.collaborators (
  invitation_id uuid not null references public.invitations(invitation_id) on delete cascade,
  user_id bigint not null references public.users(user_id),
  primary key (invitation_id, user_id)
);

create table public.errors_log (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone not null default now(),
  method text,
  message text
);

create table public.health_check (
  id uuid not null default gen_random_uuid() primary key,
  status text,
  last_updated timestamp without time zone default now()
);

