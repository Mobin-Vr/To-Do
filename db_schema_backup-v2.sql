-- 6.1 users
drop policy if exists "users_select" on public.users;
create policy "users_select" on public.users for select
  using (user_clerk_id = (auth.jwt() ->> 'sub'));

drop policy if exists "users_insert" on public.users;
create policy "users_insert" on public.users for insert
  with check (user_clerk_id = (auth.jwt() ->> 'sub'));

drop policy if exists "users_update" on public.users;
create policy "users_update" on public.users for update
  using (user_clerk_id = (auth.jwt() ->> 'sub'));

-- 6.2 categories
drop policy if exists "categories_select" on public.categories;
create policy "categories_select" on public.categories for select
  using (
    category_id = '00000000-0000-0000-0000-000000000000'::uuid
    or is_category_owner(category_id)
    or is_category_collaborator(category_id)
  );

drop policy if exists "categories_insert" on public.categories;
create policy "categories_insert" on public.categories for insert
  with check (category_owner_id = requesting_user_id());

drop policy if exists "categories_update" on public.categories;
create policy "categories_update" on public.categories for update
  using (is_category_owner(category_id))
  with check (category_owner_id = requesting_user_id());

drop policy if exists "categories_delete" on public.categories;
create policy "categories_delete" on public.categories for delete
  using (is_category_owner(category_id));

-- 6.3 tasks
drop policy if exists "tasks_select" on public.tasks;
create policy "tasks_select" on public.tasks for select
  using (can_access_task(task_id));

drop policy if exists "tasks_insert" on public.tasks;
create policy "tasks_insert" on public.tasks for insert
  with check (
    task_owner_id = requesting_user_id()
    and (
      task_category_id = '00000000-0000-0000-0000-000000000000'::uuid
      or is_category_owner(task_category_id)
      or is_category_collaborator(task_category_id)
    )
  );

drop policy if exists "tasks_update" on public.tasks;
create policy "tasks_update" on public.tasks for update
  using (can_access_task(task_id));

drop policy if exists "tasks_delete" on public.tasks;
create policy "tasks_delete" on public.tasks for delete
  using (can_access_task(task_id));

-- 6.4 invitations
drop policy if exists "invitations_select" on public.invitations;
create policy "invitations_select" on public.invitations for select
  using (invitation_owner_id = requesting_user_id());

-- 6.5 collaborators
drop policy if exists "collaborators_select" on public.collaborators;
create policy "collaborators_select" on public.collaborators for select
  using (
    user_id = requesting_user_id()
    or exists (
      select 1 from public.invitations i
      where i.invitation_id = collaborators.invitation_id
        and i.invitation_owner_id = requesting_user_id()
    )
  );

